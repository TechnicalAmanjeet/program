// Write a JavaScript program to convert weeks to seconds

// 1 week = 7 days = 168 hours = 10080 minutes = 604800 seconds

// Input  : 5
// Output : 3024000


let num_of_weeks = 15;

let totalSeconds = num_of_weeks * 604800;

console.log(`Total Seconds in ${num_of_weeks} weeks is ${totalSeconds}`);

